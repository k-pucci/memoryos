// Helper function to get the appropriate icon component
export const getCategoryIcon = (
  category: string,
  size: number = 20,
  className: string = ""
) => {
  const iconProps = { size, className };

  switch (category.toLowerCase()) {
    case "research":
      return <ResearchIcon {...iconProps} />;
    case "product":
      return <ProductIcon {...iconProps} />;
    case "user experience":
    case "ux":
      return <UserExperienceIcon {...iconProps} />;
    case "strategy":
      return <StrategyIcon {...iconProps} />;
    case "meeting":
      return <MeetingIcon {...iconProps} />;
    case "task":
      return <TaskIcon {...iconProps} />;
    case "learning":
      return <LearningIcon {...iconProps} />;
    case "idea":
      return <IdeaIcon {...iconProps} />;
    case "personal":
      return <PersonalIcon {...iconProps} />;
    case "work":
      return <WorkIcon {...iconProps} />;
    case "project":
      return <ProjectIcon {...iconProps} />;
    default:
      return <ResearchIcon {...iconProps} />; // Default fallback
  }
};

